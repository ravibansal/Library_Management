    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Iterator<carType> f4=ob1.TypeList.iterator();
        carType user1;
        int flag=0;
        car user=null;
        while(f4.hasNext()){
            user1=f4.next();
            if(jComboBox1.getSelectedItem().toString().equalsIgnoreCase(user1.getName()))
            {
                user1.setDemand();
                if(jComboBox2.getSelectedItem().toString().equalsIgnoreCase("A/C"))
                {
                    user1.getAc();
                    flag=1;
                }
                else
                {   
                    user1.getNonAc();
                    flag=0;
                }
                Iterator<car> f5=ob1.CarList.iterator();
                while(f5.hasNext()){
                    user=f5.next();
                    if(user1.getName().equalsIgnoreCase(user.getName())&&user.getState()==0&&user.getType()==flag)
                    {
                        user.setState(1);
                        user.setAdvance(Integer.parseInt(jTextField1.getText()));
                        user.setEDR(jComboBox3.getSelectedItem().toString(),jComboBox4.getSelectedItem().toString(),jComboBox5.getSelectedItem().toString(),jComboBox6.getSelectedItem().toString(),jComboBox7.getSelectedItem().toString());
                        user.setMilemeter1(Integer.parseInt(jTextField3.getText()));
                        user.setTime1(Integer.parseInt(jComboBox8.getSelectedItem().toString())*24*60+Integer.parseInt(jComboBox9.getSelectedItem().toString())*31*24*60+Integer.parseInt(jComboBox10.getSelectedItem().toString())*365*24*60+Integer.parseInt(jComboBox11.getSelectedItem().toString())*60+Integer.parseInt(jComboBox12.getSelectedItem().toString()));
                        f5.equals(user);
                        f4.equals(user1);
                        break;
                    }
                }
            }
        }
        this.hide();
        JOptionPane.showMessageDialog(null,"Have a Nice Journey !!\n"+"Your car Id is: "+user.getId());
        Main m=new Main();
        ob1.counter++;
        m.write(ob1);
    }//GEN-LAST:event_jButton2ActionPerformed

