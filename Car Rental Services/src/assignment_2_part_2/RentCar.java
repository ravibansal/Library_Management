    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Iterator<carType> f4=ob1.TypeList.iterator();
        carType user=null;
        int flag=0;
        while(f4.hasNext()){
            user=f4.next();
            if(jComboBox1.getSelectedItem().toString().equalsIgnoreCase(user.getName()))
            {
                if(jComboBox2.getSelectedItem().toString().equalsIgnoreCase("A/C"))
                {
                    if(user.getNoofac()!=0)
                    {
                        flag=1;
                    }
                    else
                         JOptionPane.showMessageDialog(null,"A/C is not available");
                }
                else
                {
                     if(user.getNoofnonac()!=0)
                    {
                        flag=1;
                    }
                    else
                         JOptionPane.showMessageDialog(null,"Non A/C is not available");
                }    
                break;
            }
        }
        if(flag==1&&user!=null)
        {
            if(jComboBox2.getSelectedItem().toString().equals("A/C"))
            {
                jLabel14.setText(Integer.toString(user.getPhc()+50));
                jLabel13.setText(Integer.toString(user.getPkc()+50));
            }
            else
            {
                jLabel14.setText(Integer.toString(user.getPhc()));
                jLabel13.setText(Integer.toString(user.getPkc()));
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

