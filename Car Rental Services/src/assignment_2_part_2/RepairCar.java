    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        for (int i = 0; i < ob1.CarList.size(); i++) {
            if(ob1.CarList.get(i).getId()==Integer.parseInt(jComboBox2.getSelectedItem().toString()))
            {
                ob1.CarList.get(i).setState(0);
                ob1.CarList.get(i).setExpense(Integer.parseInt(jTextField2.getText()));
                for (int j = 0; j < ob1.TypeList.size(); j++) {
                        if(ob1.TypeList.get(j).getName().equals(ob1.CarList.get(i).getName()))
                        {
                            if(ob1.CarList.get(i).getType()==1)
                                ob1.TypeList.get(j).setAc();
                            else
                                ob1.TypeList.get(j).setNonAc();
                        }
                        ob1.TypeList.get(j).setExpense(Integer.parseInt(jTextField2.getText()));
                }
                break;
            }
        }
        this.hide();
        Main m=new Main();
        m.write(ob1);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTextField2KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyTyped
        char c=evt.getKeyChar();
        if(!((Character.isDigit(c))|| (c==KeyEvent.VK_BACKSPACE) || (c==KeyEvent.VK_DELETE))){
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_jTextField2KeyTyped

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Vector v = new Vector();
        for (int i = 0; i < ob1.CarList.size(); i++) {
            if(jComboBox1.getSelectedItem().toString().equalsIgnoreCase(ob1.CarList.get(i).getName())&&ob1.CarList.get(i).getState()==2)
            v.add(Integer.toString(ob1.CarList.get(i).getId()));
        }
        DefaultComboBoxModel cb = new DefaultComboBoxModel(v);
        jComboBox2.setModel(cb);
    }//GEN-LAST:event_jButton2ActionPerformed
 

